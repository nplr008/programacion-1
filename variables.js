// VARIABLES
// def: son celdas de memoria dentro de un lenguaje de prog. 
// que nos permite almacenar diferentes tipos datos

// TIPOS DE VARIABLES
// 1-Numéricas
var numero1 = 5;
var numero2 = 10;
numero2 = 20;  // actualización de la variable "numero2"
// 2-Texto ó String
var saludo = "Hola!";
var despedida = 'Adios!';
// 3-Booleanas
var verdadero = true;
var falso = false;
// 4-Array
var coche = ["Seat","Fiat","BMW","KIA","Renault"];

// OPERAR CON VARIABLES
// +  :  SUMA
// -  :  RESTA 
// /  :  DIVISIÓN
// *  :  MULTIPLICACIÓN
// %  :  MODULO O RESTO 
// ++ :  INCREMENTO EN UNA UNIDAD 
// -- :  DECREMENTO EN UNA UNIDAD 

// Ejemplos:
var num1 = 10;
var num2 =  5;

var suma = num1 + num2;
var resta = num1 - num2;
var div = num1 / num2;
var mult = num1 * num2;
var resto = num1 % num2;

console.log(suma);
console.log(resta);
console.log(div);
console.log(mult);
console.log(resto);










